/**
* Class that represents a rectangle primitive
* @param id ID of the rectangle
* @param x1 coordinates
* @param x2 coordinates
* @param y1 coordinates
* @param y2 coordinates
*/
function xmlRectangle(id, x1, x2, y1, y2)
{
  this.id = id;
  this.x1 = x1;
  this.x2 = x2;
  this.y1 = y1;
  this.y2 = y2;
};
